package ex_10_FOR_Loop;
// FOR LOOP CONCEPT
public class Lab108_FOR_Loop_11 {
    public static void main(String[] args) {
        for (int i = -1; i > -10 ; i--) {
            System.out.println(i);
        }

        int i = 10;
        for (; i > 0; i--) {
            System.out.println(i);
        }
    }
}
